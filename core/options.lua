local set = vim.o

-- line number options
set.number = true
set.relativenumber = true

-- sets tab length
set.tabstop = 4
set.shiftwidth = 4
set.expandtab = true

-- sets margin below cursor when scrolling
set.scrolloff = 12

-- turn off line wrap
set.wrap = false

-- turns off highlight for previous searches
set.hlsearch = false
